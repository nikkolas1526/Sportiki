CREATE TABLE Scooters
(
    Item_id INTEGER PRIMARY KEY REFERENCES Items(Id) ON DELETE CASCADE,
    Scooters_Type scooters_type,
    Scooters_Weight INTEGER,
    Scooters_Wheel_Size INTEGER,
    Scooters_Power INTEGER,
    Scooters_Battery INTEGER,
    Scooters_Brand VARCHAR(30),
    Scooters_Country VARCHAR(30)
);


CREATE TABLE Balls
(
    Item_id INTEGER PRIMARY KEY REFERENCES Items(Id) ON DELETE CASCADE,
    Balls_Type balls_type,
    Balls_Size INTEGER,
    Balls_Brand VARCHAR(30),
    Balls_Country VARCHAR(30)
);


CREATE TABLE Protective_Equipment
(
    Item_id INTEGER PRIMARY KEY REFERENCES Items(Id) ON DELETE CASCADE,
    Protective_Equipment_Sport protective_equipment_sport,
    Protective_Equipment_Type protective_equipment_type,
    Protective_Equipment_Size protective_equipment_size,
    Protective_Equipment_Brand VARCHAR(30),
    Protective_Equipment_Country VARCHAR(30)
