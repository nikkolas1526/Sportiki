CREATE TABLE Gloves
(
    Item_id INTEGER PRIMARY KEY REFERENCES Items(Id) ON DELETE CASCADE,
    Gloves_Type gloves_type,
    Gloves_Size INTEGER,
    Gloves_Material VARCHAR(30),
    Gloves_Brand VARCHAR(30),
    Gloves_Country VARCHAR(30)
);

CREATE TABLE Accounts
(
    Id SERIAL PRIMARY KEY,
    Nickname VARCHAR(30) NOT NULL UNIQUE,
    Password VARCHAR(30) NOT NULL,
    Name VARCHAR(30),
    Surname VARCHAR(30),
    Administrator BOOLEAN NOT NULL CHECK (Administrator OR (Surname IS NOT NULL AND Name IS NOT NULL))
);


CREATE TABLE Views
(
    Viewed_Account_Id INTEGER PRIMARY KEY REFERENCES Accounts(Id) UNIQUE,
    Rate INTEGER NOT NULL CHECK (Rate BETWEEN 1 AND 5),
    ViewText TEXT
);


CREATE TABLE Liked_Items
(
    Liked_Item_Id INTEGER REFERENCES Items(Id) ON DELETE CASCADE,
    Liked_Account_Id INTEGER REFERENCES Accounts(Id),
    PRIMARY KEY (Liked_Item_Id, Liked_Account_Id)
);


CREATE TABLE Bin_Items
(
    Bin_Item_Id INTEGER REFERENCES Items(Id) ON DELETE CASCADE,
    Binned_Account_Id INTEGER REFERENCES Accounts(Id),
    PRIMARY KEY (Bin_Item_Id, Binned_Account_Id)
);


CREATE TABLE Orders 
(
    Id SERIAL PRIMARY KEY,
    Account_id INTEGER REFERENCES Accounts(Id),
    Total_Price INTEGER NOT NULL,
    Time TIMESTAMP,
    Status status_list 
);


CREATE TABLE Ordered_Items 
(
    Ordered_Item_Id INTEGER REFERENCES Items(Id) ON DELETE CASCADE,
