CREATE TABLE Items
(
    Id SERIAL PRIMARY KEY,
    Name VARCHAR(30) NOT NULL,
    Description TEXT,
    Price INTEGER NOT NULL,
    Sale INTEGER,
    Availability BOOLEAN NOT NULL,
    Photo BYTEA,
    Kategory typekritery
);


CREATE TABLE Roliks
(
    Item_id INTEGER PRIMARY KEY REFERENCES Items(Id) ON DELETE CASCADE,
    Roliks_Size INTEGER CHECK (Roliks_Size BETWEEN 30 AND 50),
    Roliks_Type roliks_type,
    Roliks_Wheels INTEGER,
    Roliks_Brand VARCHAR(30),
    Roliks_Country VARCHAR(30)
);


CREATE TABLE Bikes
(
    Item_id INTEGER PRIMARY KEY REFERENCES Items(Id) ON DELETE CASCADE,
    Bikes_Type bikes_type,
    Bikes_Power INTEGER,
    Bikes_Battery INTEGER,
    Bikes_Weight INTEGER,
    Bikes_Wheel_Size INTEGER,
    Bikes_Material VARCHAR(30),
    Bikes_Brand VARCHAR(30),
    Bikes_Country VARCHAR(30)
